package libjam.util;


/**
 * Enumeration of units used throughout libjam.
 */
public enum Unit {

    /**
     * pixel
     */
    METER,


    /**
     * pixel
     */
    PIXEL,

    /**
     * centimeter
     */
    CM,

    /**
     * millimeter
     */
    MM,

    /**
     * milliseconds
     */
    MS,

    /**
     * seconds
     */
    SECONDS


}